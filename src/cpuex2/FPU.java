	public static float fsqr(float a) {
		int ai = Float.floatToIntBits(a);
		
		int sign, exp, man, key, cons, gradi, term, frac, result;
		sign = ai & 0x80000000;
		exp = (ai >> 23) & 0xff;
		man = ai & 0x7fffff;
		
		key = (man >> 14) & 0x1ff;
		if ((exp & 1) != 0) {
			key |= 0x200;
		}
		
		cons = sqr_table[key][0];
		gradi = sqr_table[key][1];
		
		term = (man & 0x3FFF) * gradi;
		
		if ((exp & 1) != 0)
			term >>= 14;
		else
			term >>= 13;
		
		frac = cons + term;
		if (exp == 0) {
			exp = 0;
		} else if ((exp & 1) != 0) {
			exp = exp / 2 + 64;
		} else {
			exp = exp / 2 + 63;
		}
		
		exp <<= 23;
		frac &= 0x7FFFFF;
		
		result = sign | exp | frac;
		
		return Float.intBitsToFloat(result);
	}
	
	public static float fneg(float a) {
		return -a;
	}
